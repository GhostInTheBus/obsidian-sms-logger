def _decorated_contact(c: dict) -> dict:
    """Shape a get_all_contacts() row for the templates."""
    name = c["name"]
    initials = "".join(w[0].upper() for w in name.split()[:2]) if name else "?"
    return {
        "phone": c["key"], "phone_enc": _phone_enc(c["key"]), "name": name,
        "initials": initials, "notes": "",
        "memory_count": c["message_count"],  # template shows this count badge
        "last_seen": _relative_time(c["last_seen"], datetime.utcnow()) if c["last_seen"] else None,
    }


