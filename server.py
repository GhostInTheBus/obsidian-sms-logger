@app.route("/api/messages/<contact_key>")
@limiter.exempt
def api_messages(contact_key: str):
    auth_err = _require_auth()
    if auth_err: return auth_err
    key = resolve_key(contact_key)
    since = request.args.get("since", "").strip()
    limit = min(int(request.args.get("limit", "100")), 1000)
    order = request.args.get("order", "asc")
    msgs = get_message_history(key, limit=limit)  # oldest first
    if since:
        msgs = [m for m in msgs if (m["timestamp"] or "") > since]
    if order == "desc":
        msgs = list(reversed(msgs))
    return jsonify({"contact": key, "name": get_contact_name(key), "messages": msgs})


@app.route("/api/search")
@limiter.exempt
def api_search():
    auth_err = _require_auth()
    if auth_err: return auth_err
    q = request.args.get("q", "").strip()
    if not q:
        return jsonify({"error": "q required"}), 400
    contact = request.args.get("contact", "").strip()
    limit = min(int(request.args.get("limit", "50")), 200)
    key = resolve_key(contact) if contact else None
    index = load_index()
    results = []
    for r in search_messages(q, phone=key, limit=limit):
        r["name"] = (index.get(r["phone"]) or {}).get("name") or r["phone"]
        results.append(r)
    return jsonify({"query": q, "results": results})


@app.route("/api/recent")
@limiter.exempt
def api_recent():
    """Most recent observed messages across ALL contacts (an assistant's silent-watch feed)."""
    auth_err = _require_auth()
    if auth_err: return auth_err
    limit = min(int(request.args.get("limit", "15")), 100)
    with get_db() as conn:
        rows = conn.execute(
            "SELECT phone, content, timestamp FROM observed_messages ORDER BY id DESC LIMIT ?",
            (limit,),
        ).fetchall()
    index = load_index()
    messages = [
        {"phone": p, "name": (index.get(p) or {}).get("name") or p, "content": c, "timestamp": t}
        for p, c, t in reversed(rows)
    ]
    return jsonify({"messages": messages})


